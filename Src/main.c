int main(void)
{

  /* USER CODE BEGIN 1 */
	uint32_t timer = HAL_GetTick();
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_ADC3_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM5_Init();
  MX_TIM8_Init();
  MX_TIM12_Init();

  /* USER CODE BEGIN 2 */

  /*## Start the conversion process #######################################*/
  if(HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc1_Buffer, ADC1_CHANNELS_NUM * ADC_BUFFER_LENGTH) != HAL_OK)
  {
    /* Start Conversation Error */
    Error_Handler();
  }
  if(HAL_ADC_Start_DMA(&hadc3, (uint32_t*)adc3_Buffer, ADC3_CHANNELS_NUM * ADC_BUFFER_LENGTH) != HAL_OK)
  {
    /* Start Conversation Error */
    Error_Handler();
  }


  // Start OUT3
  if(HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_1)) {
	Error_Handler();
  }
  // Start OUT6
  if(HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_1)) {
	Error_Handler();
  }
  // Start OUT2
  if(HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_1)) {
	Error_Handler();
  }
  // Start OUT7
  if(HAL_TIM_PWM_Start(&htim5,TIM_CHANNEL_4)) {
	Error_Handler();
  }
  // Start OUT1
  if(HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_1)) {
	Error_Handler();
  }
  // Start OUT0
  if(HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_2)) {
	Error_Handler();
  }
  // Start OUT5
  if(HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_4)) {
	Error_Handler();
  }
  // Start OUT4
  if(HAL_TIM_PWM_Start(&htim12,TIM_CHANNEL_1)) {
	Error_Handler();
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if (HAL_GetTick() - timer > 500) {
			timer = HAL_GetTick();
//			printAdcValues();
		}

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Configure the main internal regulator output voltage 
    */
  __HAL_RCC_PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE3);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_HSI;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}


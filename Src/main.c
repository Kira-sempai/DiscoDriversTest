static void printAdcValues(void) {
//	static uint16_t i;
	char data[100];

	const int size = snprintf(data, 100, "%06lu	%u	%u	%u	%u	%u	%u\n\r",
			HAL_GetTick(),
			adc1_value[0],
			adc3_value[0],
			adc3_value[1],
			adc3_value[2],
			adc3_value[3],
			adc3_value[4]);

	if (size > 0) {
		printMessage((uint8_t*)data, size + 1);
	}
}

static void printMessage(uint8_t * const data, const uint16_t len) {
	HAL_UART_Transmit_DMA(&huart6, data, len);
}

/**
  * @brief  Error ADC callback.
  * @note   In case of error due to overrun when using ADC with DMA transfer
  *         (HAL ADC handle paramater "ErrorCode" to state "HAL_ADC_ERROR_OVR"):
  *         - Reinitialize the DMA using function "HAL_ADC_Stop_DMA()".
  *         - If needed, restart a new ADC conversion using function
  *           "HAL_ADC_Start_DMA()"
  *           (this function is also clearing overrun flag)
  * @param  hadc: pointer to a ADC_HandleTypeDef structure that contains
  *         the configuration information for the specified ADC.
  * @retval None
  */
void HAL_ADC_ErrorCallback(ADC_HandleTypeDef *hadc)
{
	uint8_t message[] = "All bad!\n\r";
	printMessage(message, (uint16_t)sizeof(message));
}


uint32_t accumulate(uint16_t* first, uint16_t* last, uint32_t init)
{
    for (; first != last; ++first) {
        init = init + *first;
    }
    return init;
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* AdcHandle)
{
	if (AdcHandle == &hadc1) {
		adc1_value[0] = accumulate(adc1_Buffer, adc1_Buffer + ADC_BUFFER_LENGTH, 0) / ADC_BUFFER_LENGTH;
	} else
	if (AdcHandle == &hadc3) {
		for (uint16_t i = 0; i < ADC3_CHANNELS_NUM; ++i) {
			uint32_t value = 0;
			for (uint16_t j = 0; j < ADC_BUFFER_LENGTH; ++j) {
				value += adc3_Buffer[j * ADC3_CHANNELS_NUM + i];
			}
			adc3_value[i] = value / ADC_BUFFER_LENGTH;
		}
	}
}
